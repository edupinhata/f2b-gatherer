#DATABASE OF FAIL2BAN 

CREATE DATABASE IF NOT EXISTS F2B_LOGS_DB;

USE F2B_LOGS_DB;


CREATE TABLE LOGS(
	LOG_MAC INT,
	LOG_DATE DATE,
	LOG_TIME TIME,
	LOG_RESOURCE VARCHAR(50),
	LOG_PID VARCHAR(20),
	LOG_STATUS VARCHAR(10),
	LOG_ACTION VARCHAR(20),
	CONSTRAINT LINE PRIMARY KEY (MAC, LOG_DATE, LOG_TIME) 
)







